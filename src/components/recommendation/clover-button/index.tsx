import { motion } from "framer-motion";
import { type PropsWithChildren, useMemo } from "react";
import { cn } from "@/lib/utils";

interface Props {
  active: boolean;
  selected: boolean;
  onClick: () => void;
  position: "top-left" | "top-right" | "bottom-left" | "bottom-right";
}

export const CloverButton = ({
  active,
  selected,
  onClick,
  position,
  children,
}: PropsWithChildren<Props>) => {
  const color = active ? "#4B5563" : "#202228";

  const edge = useMemo(() => {
    switch (position) {
      case "top-left":
        return <TopLeft color={color} selected={selected} />;
      case "top-right":
        return <TopRight color={color} selected={selected} />;
      case "bottom-left":
        return <BottomLeft color={color} selected={selected} />;
      case "bottom-right":
        return <BottomRight color={color} selected={selected} />;
    }
  }, [color, selected, position]);

  const spacing = useMemo(() => {
    switch (position) {
      case "top-left":
        return "pl-[30px] pr-[20px] pt-[68px] pb-[50px]";
      case "top-right":
        return "pr-[30px] pl-[20px] pt-[68px] pb-[50px]";
      case "bottom-left":
        return "pl-[30px] pr-[20px] pb-[68px] pt-[50px]";
      case "bottom-right":
        return "pr-[30px] pl-[20px] pb-[68px] pt-[50px]";
    }
  }, [position]);

  const axis = useMemo(() => {
    switch (position) {
      case "top-left":
        return { x: -20, y: -20 };
      case "top-right":
        return { x: 20, y: -20 };
      case "bottom-left":
        return { x: -20, y: 20 };
      case "bottom-right":
        return { x: 20, y: 20 };
    }
  }, [position]);

  return (
    <motion.button
      type="button"
      onClick={onClick}
      initial={{ x: axis.x, y: axis.y, opacity: 0 }}
      animate={{ x: 0, y: 0, opacity: 1 }}
      exit={{ x: axis.x, y: axis.y, opacity: 0 }}
      transition={{ duration: 0.25, ease: "easeOut" }}
      className="relative aspect-square w-full focus:outline-none"
    >
      <div className="absolute z-10 h-full w-full">{edge}</div>
      <div
        className={cn(
          "relative z-20 flex h-full w-full items-center justify-center break-keep text-subtitle-18-semibold transition-colors",
          !active && !selected && "text-gray-600",
          active && !selected && "text-gray-400",
          selected && "text-primary-500",
          spacing,
        )}
      >
        {children}
      </div>
    </motion.button>
  );
};

